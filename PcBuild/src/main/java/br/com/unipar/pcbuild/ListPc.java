    private void preencheData() {

        model = (DefaultTableModel) jTable1.getModel();
        ((DefaultTableModel) jTable1.getModel()).setRowCount(0);

        while (!pilhaPc.isEmpty()) {
            for (int i = 0; i < CadastroPc.pilhaPc.top().getPecas().size(); CadastroPc.pilhaPc.pop()) {

                model.addRow(new Object[]{
                    CadastroPc.pilhaPc.top().getPecas().get(i).getNome(),
                    CadastroPc.pilhaPc.top().getVlTotal(),});
            }

        }
        //setar tamanho da coluna 2 como 20
        TableColumn col = jTable1.getColumnModel().getColumn(1);
        int width = 20;

        col.setPreferredWidth(width);
    }

